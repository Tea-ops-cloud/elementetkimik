
    function kontrolloPergjigjen(eshteSakte) {
        let rezultati = document.getElementById("rezultati");
        if (eshteSakte) {
            rezultati.innerHTML = "Përgjigja e saktë është Jodi!";
            rezultati.style.color = "green";
        } else {
            rezultati.innerHTML = "Gabim! Përgjigja e saktë është Jodi.";
            rezultati.style.color = "red";
        }
    }

